package com.cg.bean;

public class Calculator
{
	public int add(int num1, int num2)
	{
		return num1+num2;
	}
	public int subtract (int num1,int num2)
	{
		return num1-num2;
		
	
	}
	public int multiple(int num1,int num2)
		{
			return num1*num2;
		}
	public int calcSquare(int num)
	{
		return num*num;
	}
	
}
